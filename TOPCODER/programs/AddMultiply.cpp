class AddMultiply {
public:
	vector <int> makeExpression(int);
};

vector <int> AddMultiply::makeExpression(int y) {
	int x0, x1, x2, rhs, lhs;
	bool flag = false;
	for(x2=-1000;x2<=1000;x2++){
		if(x2!=0 && x2!=1){
			rhs = y-x2;
			for(x0=-1000;x0<=1000;x0++){
				if(x0!=0 && x0 != 1){
					if(rhs%x0==0 && rhs/x0!=0 && rhs/x0!=1){
						//cout << y%x0 << " " << y/x0 << " " << endl;
						x1 = rhs/x0;
						flag = true;
						break;
					}
				}
			}
			if(flag==true)
				break;
		}
	}
	vector<int> ret;
	ret.push_back(x0);
	ret.push_back(x1);
	ret.push_back(x2);
	return ret;
}


